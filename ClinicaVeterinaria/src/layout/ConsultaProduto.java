    private void jTableConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableConsultaMouseClicked
        Consulta c = new Consulta();
        Produto p = new Produto();

        //captura o quantidade de cliques
        if (evt.getClickCount() == 2) {
            int codigo = (int) (jTableConsulta.getModel().getValueAt(jTableConsulta.getSelectedRow(), 0));
            int tipoProduto = (int) jTableConsulta.getModel().getValueAt(jTableConsulta.getSelectedRow(), 4);
            /*
             pega o codigo do produto e consulta no banco suas informações 
             depedendo do tipo do produto e solicitado um metodo
             */
            if (tipoProduto == 1) {
                p = c.preencheMaterial(codigo);
            } else if (tipoProduto == 2) {
                p = c.preencheLimpeza(codigo);
            } else if (tipoProduto == 3) {
                p = c.preencheMedicamento(codigo);
            }
            /*
             seta os valores da consulta no campos
             */
            System.out.println(p.getTipoProduto());
            System.out.println(codigo + " " + tipoProduto);
            txtCodigo1.setText(String.valueOf(codigo));
            txtLab.setText(p.getLaboratorio());
            txtStatus.setText(p.getEstado());
            txtTarja.setText(p.getTarja());
            txtEspecie.setText(p.getTipoAnimal());
            txtTipoMedicamento.setText(c.TipoMedicamento(tipoProduto));

        }

    }//GEN-LAST:event_jTableConsultaMouseClicked

