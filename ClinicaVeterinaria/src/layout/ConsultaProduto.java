    private void btnConsultaCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaCodigoActionPerformed
        ValidaConsulta c = new ValidaConsulta();
        /*Consulta Codigo e ja seta na tebela padrao(CODIGO, NOME, QUANTIDA, PRODUTO, VALOR, TIPO)*/
        //valida os dados da consulta
        if (c.validaCampos(txtCodigo.getText())) {

            Consulta consultCod = new Consulta();
            consultCod.iniciarTabelaPadrao(jTableConsulta, consultCod.consultarCodigo(Integer.parseInt(txtCodigo.getText())));
            jTableConsulta.setEnabled(true);
            jTableConsulta.setForeground(Color.black);
        } else {
            //mostra mensagem de validação
            c.msgmCodigo();
        }

    }//GEN-LAST:event_btnConsultaCodigoActionPerformed

