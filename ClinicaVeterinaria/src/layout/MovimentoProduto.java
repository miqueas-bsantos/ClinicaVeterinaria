    private void btnAtualizarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarMaterialActionPerformed
        ValidaMovimentacao m = new ValidaMovimentacao();
        Movimento movimentar = new Movimento();
        Consulta c = new Consulta();
        Produto p = new Produto();

        if (m.validarCampo(txtQuantidade.getText(), txtCodigo.getText())) {
            movimentar.setCodigoFuncionario(user.getCodigo());
            movimentar.setCodigoProduto(Integer.parseInt(txtCodigo.getText()));
            movimentar.setTipoMovimento(3);//Esterialização
            movimentar.setQuantidadeMovimento(Integer.parseInt(txtQuantidade.getText()));
            System.out.println(jCombMaterialAtualizar.getSelectedItem() + "");
            if (m.validaMaterial(jCombMaterialAtualizar.getSelectedItem() + "")) {
                if (c.consultarQuantidadeItem(movimentar.getCodigoProduto())!=0) {
                    movimentar.estrializarMaterial(Integer.parseInt(txtCodigo.getText()), jCombMaterialAtualizar.getSelectedItem() + "", movimentar);
                    p = c.preencheMaterial(Integer.parseInt(txtCodigo.getText()));
                    txtStatus.setText(p.getEstado());
                }else{
                    m.msgmAtulizarInvalida();
                }
            } else {
                m.mostrarMsgmMaterial();
            }

        } else {
            m.mostrarMsgm(txtQuantidade.getText(), txtCodigo.getText());
        }

    }//GEN-LAST:event_btnAtualizarMaterialActionPerformed

