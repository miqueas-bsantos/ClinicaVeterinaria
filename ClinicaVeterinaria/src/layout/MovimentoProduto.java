    private void btnSaidaProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaidaProdutoActionPerformed
        ValidaMovimentacao m = new ValidaMovimentacao();
        Movimento movimentar = new Movimento();
        Consulta consultCompleta = new Consulta();

        movimentar.setCodigoFuncionario(user.getCodigo());
        System.out.println(user.getCodigo());
        movimentar.setCodigoProduto(Integer.parseInt(txtCodigo.getText()));
        movimentar.setTipoMovimento(2);//Saida
        movimentar.setQuantidadeMovimento(Integer.parseInt(txtQuantidade.getText()));

        if (m.validarCampo(txtQuantidade.getText(), txtCodigo.getText())) {
            movimentar.setCodigoFuncionario(user.getCodigo());
            System.out.println(user.getCodigo());
            movimentar.setCodigoProduto(Integer.parseInt(txtCodigo.getText()));
            movimentar.setTipoMovimento(2);//Saida
            movimentar.setQuantidadeMovimento(Integer.parseInt(txtQuantidade.getText()));
            System.out.println(jCombMaterialAtualizar.getSelectedItem() + "");

            movimentar.saida(movimentar.getQuantidadeMovimento(), movimentar.getCodigoProduto(), movimentar);
            consultCompleta.iniciarTabelaPadrao(jTable1, consultCompleta.consultarCodigo(movimentar.getCodigoProduto()));

            int quantMax = consultCompleta.quantMax(movimentar.getCodigoProduto());
            int quantMin = consultCompleta.quantMin(movimentar.getCodigoProduto());
            int quantidadeItem = consultCompleta.consultarQuantidadeItem(movimentar.getCodigoProduto());

            if (quantidadeItem > quantMax) {
                m.mostraMsgmAlta();
                jTable1.setForeground(Color.red);
                //System.out.println(consultCompleta.consultarQuantidadeItem(movimentar.getCodigoProduto()));
            } else if(quantidadeItem < quantMin){
                m.mostraMsgmBaixa();
                jTable1.setForeground(Color.red);
            } else {
                jTable1.setForeground(Color.BLACK);
                //System.out.println(consultCompleta.consultarQuantidadeItem(movimentar.getCodigoProduto()));
            }

        } else {
            m.mostrarMsgm(txtStatus.getText(), txtCodigo.getText());
        }

    }//GEN-LAST:event_btnSaidaProdutoActionPerformed

